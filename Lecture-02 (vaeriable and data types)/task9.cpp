#include <iostream>
using namespace std;
// relational operator return as an boolean value true false
int main()
{
    int a = 5, b = 7;
    cout << (a > b) << endl;
    cout << (a < b) << endl;
    cout << (a >= b) << endl;
    cout << (a <= b) << endl;
    cout << (a != b) << endl;
    return 0;
}