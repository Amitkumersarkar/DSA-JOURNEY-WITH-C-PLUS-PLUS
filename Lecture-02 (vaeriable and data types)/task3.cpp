#include <iostream>
using namespace std;

// primitive data types in c++
// dsa

int main()
{
    // 8 bit = 1 bytes
    int age = 25;          // 4 bytes
    char num = 'A';        // 1 bytes
    float PI = 3.14f;      // 4bytes
    bool isSafe = true;    // 1 bytes
    bool notSafe = false;  // 1 bytes
    double grade = 100.55; // 8 bytes
    cout << PI << endl;
    cout << isSafe << " " << notSafe << endl;

    return 0;
}